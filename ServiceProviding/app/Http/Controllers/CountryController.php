    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $status = false;
        if ($request->has('country_name') && $request->has('country_code')) {
            $country = new Country();
            $country->name = $request['country_name'];
            $country->phone_code = $request['country_code'];

            $result = $country->save();
            if ($result) {
                $status = true;
            }
        }

        return redirect('country/add')->with('add_country_status', $status);
    }

